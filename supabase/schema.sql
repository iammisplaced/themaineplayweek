create table if not exists public.theatres (
  id bigint generated by default as identity primary key,
  name text not null,
  city text not null,
  address text not null,
  website text not null,
  latitude double precision,
  longitude double precision,
  created_at timestamptz not null default now()
);

alter table public.theatres add column if not exists latitude double precision;
alter table public.theatres add column if not exists longitude double precision;

create table if not exists public.films (
  id bigint generated by default as identity primary key,
  title text not null,
  year integer,
  tmdb_id bigint,
  ticket_link text not null default '',
  staff_favorite boolean not null default false,
  staff_favorite_by text not null default '',
  featured_on_playweek boolean not null default false,
  featured_on_playweek_url text not null default '',
  metadata_source text not null default 'manual',
  tmdb_json jsonb,
  created_at timestamptz not null default now()
);

alter table public.films add column if not exists staff_favorite boolean not null default false;
alter table public.films add column if not exists staff_favorite_by text not null default '';
alter table public.films add column if not exists featured_on_playweek boolean not null default false;
alter table public.films add column if not exists featured_on_playweek_url text not null default '';
alter table public.films add column if not exists metadata_source text;

update public.films
set metadata_source = case
  when tmdb_id is not null then 'tmdb'
  else 'manual'
end
where metadata_source is null
   or btrim(metadata_source) = '';

update public.films
set metadata_source = 'manual'
where metadata_source not in ('tmdb', 'manual');

alter table public.films alter column metadata_source set default 'manual';
alter table public.films alter column metadata_source set not null;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'films_metadata_source_check'
      and conrelid = 'public.films'::regclass
  ) then
    execute $sql$
      alter table public.films
      add constraint films_metadata_source_check
      check (metadata_source in ('tmdb', 'manual'))
    $sql$;
  end if;
end $$;

create table if not exists public.theatre_films (
  theatre_id bigint not null references public.theatres(id) on delete cascade,
  film_id bigint not null references public.films(id) on delete cascade,
  ticket_link text not null default '',
  created_at timestamptz not null default now(),
  primary key (theatre_id, film_id)
);

create table if not exists public.showings (
  id bigint generated by default as identity primary key,
  theatre_id bigint not null references public.theatres(id) on delete cascade,
  film_id bigint not null references public.films(id) on delete cascade,
  show_date date not null,
  times text[] not null,
  created_at timestamptz not null default now()
);

create table if not exists public.promos (
  id bigint generated by default as identity primary key,
  title text not null default '',
  button_url text not null default '',
  image_path text not null,
  image_alt text not null default '',
  image_name text not null default '',
  button_label text not null default 'Shop Now',
  enabled boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now()
);

alter table public.promos add column if not exists image_path text;

do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'promos'
      and column_name = 'image_src'
  ) then
    execute $sql$
      update public.promos
      set image_path = coalesce(
        image_path,
        case
          when image_src like 'data:%' then ''
          else image_src
        end,
        ''
      )
      where image_path is null
    $sql$;
    execute 'alter table public.promos drop column image_src';
  end if;
end $$;

update public.promos set image_path = '' where image_path is null;
alter table public.promos alter column image_path set not null;

